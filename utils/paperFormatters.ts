export const getValidPaperUrls = (): string[] => {
  const allPaperIds = Array.from(Array(197).keys());
  return allPaperIds.map((paperId) => {
    if (paperId === 0) {
      return "foreword";
    }

    return `paper-${paperId}-${paperIdToKebabPaperTitle(`${paperId}`)}`;
  });
};

export const getPaperIdFromPaperUrl = (url: string): string => {
  // If it's the foreword, return 0.
  if (url === "foreword") {
    return "0";
  }

  // Extract the id from the URL, e.g. "paper-1-the-universal-father" -> "1"
  const allPaperIds = Array.from(Array(197).keys());
  const paperId = url.split("-")[1];
  if (allPaperIds.includes(parseInt(paperId))) {
    return paperId;
  }

  // If it's not a valid paper ID, return an empty string.
  return "";
};

export const paperIdToUrl = (paperId: string): string => {
  if (paperId === "0") {
    return "foreword";
  }

  // Return back what was passed in if it's not a valid paper ID, it might be a kebab case title already.
  const allPaperIds = Array.from(Array(197).keys());
  if (!allPaperIds.includes(parseInt(paperId))) {
    return paperId;
  }

  // If it's a valid paper ID, return the kebab case title.
  return `paper-${paperId}-${paperIdToKebabPaperTitle(paperId)}`;
};
